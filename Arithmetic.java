class Arithmetic
{
	public static void main(String args[])
	{
		int a = 10, b = 5;
		System.out.println ("a = 10, b = 5");
		int add, sub, mul, div, mod;
		add = a+b;
		sub = a-b;
		mul = a*b;
		div = a/b;
		mod = a%b;
		System.out.println ("Addition = " + add + " Subtraction = " + sub + " Multiplication = " + mul + " Division = " + div + "Modulus = " + mod);
	}
}